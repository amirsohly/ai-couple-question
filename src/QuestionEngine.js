function random(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

/* مفاهیم مخصوص زوج‌ها */
const concepts = {
  desire: [
    "میل جنسی",
    "برانگیختگی",
    "کشش",
  ],
  satisfaction: [
    "رضایت جنسی",
    "لذت",
    "احساس کامل بودن",
  ],
  expression: [
    "بیان خواسته جنسی",
    "گفتن ترجیح‌ها",
    "شفافیت در نیاز",
  ],
  connection: [
    "ارتباط احساسی",
    "امنیت",
    "صمیمیت",
  ],
};

/* قالب‌های بسیار صریح (اما غیرگرافیکی) */
const coupleBoldFrames = [

  // 1. تفاوت میل
  () =>
    `احساس می‌کنی میل جنسی تو و پارتنرت معمولاً هم‌سطح است یا متفاوت؟ این تفاوت چطور روی رابطه‌تون اثر می‌ذاره؟`,

  // 2. رضایت واقعی
  () =>
    `در رابطه‌تون، چه چیزی بیشترین نقش رو در حس ${random(
      concepts.satisfaction
    )} تو داره؟`,

  // 3. خواسته‌های نگفته
  () =>
    `آیا خواسته جنسی‌ای هست که دوست داشته باشی تجربه کنی ولی هنوز نگفتی؟ دلیلش چیه؟`,

  // 4. ذهن یا بدن
  () =>
    `برای تو در رابطه جنسی، تحریک بیشتر از ذهن شروع می‌شه یا بدن؟`,

  // 5. بعد از صمیمیت
  () =>
    `بعد از یک رابطه صمیمی خوب، چه حسی بیش از همه بهت دست می‌ده؟`,

  // 6. ارتباط و میل
  () =>
    `وقتی ارتباط احساسی‌تون قوی‌تره، ${random(
      concepts.desire
    )} تو چه تغییری می‌کنه؟`,

  // 7. بیان مستقیم
  () =>
    `چقدر راحت می‌تونی درباره ${random(
      concepts.expression
    )} با پارتنرت حرف بزنی؟`,

  // 8. رضایت دوطرفه
  () =>
    `از کجا می‌فهمی پارتنرت از نظر جنسی واقعاً راضیه؟`,

  // 9. مرزها
  () =>
    `آیا مرزی در رابطه جنسی‌تون هست که دوست داری بهتر دربارش صحبت بشه؟`,

  // 10. تغییر با زمان
  () =>
    `به نظرت میل و رضایت جنسی‌تون با گذر زمان بیشتر شده یا تغییر شکل داده؟`,
];

let lastQuestions = [];

export function generateQuestion(mode = "couple-bold") {
  let q;
  let guard = 0;

  if (mode === "couple-bold") {
    q = random(coupleBoldFrames)();
  } else {
    q = "سؤال نامعتبر";
  }

  while (lastQuestions.includes(q) && guard < 10) {
    q = random(coupleBoldFrames)();
    guard++;
  }

  lastQuestions.push(q);
  if (lastQuestions.length > 5) lastQuestions.shift();

  return q;
}
